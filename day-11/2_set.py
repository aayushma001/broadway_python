# Set in python is mutable. However, Every elements of set are immutable
# creating a  empty set
a = set() # This is a proper way of creating an empty set.
# a = {} # this is not an empty set rather than it is a dictionary
a = set([1, 2, 3, 4])
print(a) # {1, 2, 3, 4)
a = {1, 2, "a", "b", "apple", True }
print(a)
